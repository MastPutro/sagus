package com.example.sagu.katering;

public class itemCat {
    String namacust, phone, foods, drinks, rice, amount, address, tanggalcat, idcat;
    Boolean status;
    public itemCat() {
        this.namacust = null;
        this.phone = null;
        this.foods = null;
        this.drinks = null;
        this.rice = null;
        this.amount = null;
        this.address = null;
        this.tanggalcat = null;
        this.idcat = null;
        this.status = null;
    }

    public String getNamacust() {
        return namacust;
    }

    public void setNamacust(String namacust) {
        this.namacust = namacust;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getFoods() {
        return foods;
    }

    public void setFoods(String foods) {
        this.foods = foods;
    }

    public String getDrinks() {
        return drinks;
    }

    public void setDrinks(String drinks) {
        this.drinks = drinks;
    }

    public String getRice() {
        return rice;
    }

    public void setRice(String rice) {
        this.rice = rice;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTanggalcat() {
        return tanggalcat;
    }

    public void setTanggalcat(String tanggalcat) {
        this.tanggalcat = tanggalcat;
    }

    public String getIdcat() {
        return idcat;
    }

    public void setIdcat(String idcat) {
        this.idcat = idcat;
    }

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }
}
